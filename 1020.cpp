#include <bits/stdc++.h>

using namespace std;

int main(){
	int days = 0, months = 0, years = 0;
	cin >> days;
	if(days >= 365){
		years = days/365;
		days %= 365;
	}
	if(days >= 30){
		months = days/30;
		days %= 30;
	}
	printf("%d ano(s)\n", years);
	printf("%d mes(es)\n", months);
	printf("%d dia(s)\n", days);
}
